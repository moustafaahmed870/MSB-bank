function updateContent() {
    if (!i18nextInitialized) {
        console.warn('i18next غير مهيء بعد');
        return;
    }
    
    try {
        const newDir = i18next.language === 'ar' ? 'rtl' : 'ltr';
        document.documentElement.dir = newDir;
        document.documentElement.lang = i18next.language;
        
        const currentLanguageElement = document.getElementById('current-language');
        if (currentLanguageElement) {
            currentLanguageElement.textContent = i18next.language === 'ar' ? 'العربية' : 'English';
        }
        
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (key) {
                const translation = i18next.t(key);
                if (translation && translation !== key) {
                    element.textContent = translation;
                }
            }
        });
        
        if (window.updateCurrencyNames) {
            window.updateCurrencyNames();
        }
        
        if (window.app && window.app.currentPage === 'rates') {
            if (window.loadExchangeRatesWithTranslation) {
                setTimeout(() => {
                    window.loadExchangeRatesWithTranslation();
                }, 500);
            }
        }
        
        console.log('تم تحديث المحتوى للغة:', i18next.language);
    } catch (error) {
        console.error('خطأ في تحديث المحتوى:', error);
    }
}

function switchLanguage(event) {
    if (event) {
        event.preventDefault();
        event.stopPropagation();
    }
    
    if (!i18nextInitialized) {
        console.error('i18next غير مهيء');
        return;
    }
    
    const newLang = i18next.language === 'ar' ? 'en' : 'ar';
    console.log('تغيير اللغة من', i18next.language, 'إلى', newLang);
    
    i18next.changeLanguage(newLang, function(err, t) {
        if (err) {
            console.error('خطأ في تغيير اللغة:', err);
            return;
        }
        updateContent();
    });
}


